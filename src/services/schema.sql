-- Create the PROFILES table
CREATE TABLE profiles (
    user_id TEXT PRIMARY KEY,
    total_items_scanned INTEGER DEFAULT 0,
    pending_points INTEGER DEFAULT 0,
    approved_points INTEGER DEFAULT 0
);

-- Create the SCANS_HISTORY table
CREATE TABLE scans_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    user_id TEXT REFERENCES profiles(user_id),
    item_name TEXT,
    category TEXT, -- (Clothing, Footwear, etc.)
    final_decision TEXT,
    points_awarded INTEGER
);

-- Create the LOCATIONS table
CREATE TABLE locations (
    id INTEGER PRIMARY KEY,
    name TEXT,
    address TEXT,
    latitude FLOAT,
    longitude FLOAT
);

-- Create the LOCATION_DEMAND table
CREATE TABLE location_demand (
    location_id INTEGER REFERENCES locations(id),
    category_name TEXT,
    demand_level TEXT, -- e.g., 'High', 'Medium', 'Low'
    PRIMARY KEY (location_id, category_name)
);

