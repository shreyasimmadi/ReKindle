--Custom Functions

-- Function: Suggest Best Location based on Category Demand
CREATE OR REPLACE FUNCTION suggest_best_location(p_categories TEXT[])
RETURNS TABLE(id INT, name TEXT, address TEXT, latitude FLOAT, longitude FLOAT, matching_categories INT, total_score BIGINT) AS $$
BEGIN
  RETURN QUERY
  SELECT
    l.id,
    l.name,
    l.address,
    l.latitude,
    l.longitude,
    COUNT(d.category_name)::INT AS matching_categories,
    -- Calculate a score based on demand level
    SUM(CASE d.demand_level
          WHEN 'High' THEN 3
          WHEN 'Medium' THEN 2
          WHEN 'Low' THEN 1
          ELSE 0
        END) AS total_score
  FROM locations l
  JOIN location_demand d ON l.id = d.location_id
  WHERE d.category_name = ANY(p_categories)
  GROUP BY l.id, l.name, l.address, l.latitude, l.longitude
  ORDER BY total_score DESC, matching_categories DESC
  LIMIT 1;
END;
$$ LANGUAGE plpgsql;

-- Function: Increment Pending Points and Scan Count
CREATE OR REPLACE FUNCTION increment_pending_points(user_id_input TEXT, point_amount INTEGER)
RETURNS void AS $$
BEGIN
  UPDATE profiles
  SET
    pending_points = pending_points + point_amount,
    total_items_scanned = total_items_scanned + 1
  WHERE user_id = user_id_input;
END;
$$ LANGUAGE plpgsql;